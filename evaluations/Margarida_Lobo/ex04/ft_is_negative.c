#include <unistd.h>

void	ft_is_negative(int n)

{
	char	c;
	char	i;

	c = 'P';
	i = 'N';
	if (n >= 0)
	{
		write(1, &c, 1);
	}
	else
	{
		write(1, &i, 1);
	}
}
