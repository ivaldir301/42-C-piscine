int	ft_recursive_power(int nb, int power)
{
	if (power < 0)
		return (0);
	if (power == 0)
		return (1);
	return (nb * ft_recursive_power(nb, power - 1));
}

#include <stdio.h>
int	main(void)
{
	int nb = 3;
	int power = 3;

	printf("%i", ft_recursive_power(nb, power));
	return (0);
}

