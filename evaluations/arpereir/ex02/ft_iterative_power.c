int	ft_iterative_power(int nb, int power)
{
	int	result;

	if (power < 0)
		return (0);
	if (power == 0)
		return (1);
	result = 1;
	while (power > 0)
	{
		result *= nb;
		power--;
	}
	return (result);
}

#include <stdio.h>

int	main(void)
{
	int nb = 3;
	int power = 3;

	printf("%i\n", ft_iterative_power(nb, power));
	return (0);
}

